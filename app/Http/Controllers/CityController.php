     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request ->validate([
            'country_id'=>'required',
            'name'=>'required'
        ]);

        $input = $request->all();
        City::create($input);

        return redirect()->route('cities.index')
            ->with('success','city added successfully');
    }

